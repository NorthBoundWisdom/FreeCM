import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDirectory = path.dirname(fileURLToPath(import.meta.url));
const extensionRoot = path.resolve(scriptDirectory, "..");
const repositoryRoot = path.resolve(extensionRoot, "..");
const contractPath = path.join(
  repositoryRoot,
  "freecm",
  "lock-schema-contract.json",
);
const outputPath = path.join(extensionRoot, "src", "lockSchema.ts");
const packagePath = path.join(extensionRoot, "package.json");

function json(value) {
  return JSON.stringify(value, null, 2);
}

function render(contract) {
  return `// Generated by scripts/write-lock-schema.mjs from freecm/lock-schema-contract.json.
// Do not edit this file by hand; run npm run generate:lock-schema.

export const LOCK_SCHEMA_VERSION = ${json(contract.schemaVersion)};

export const DEPENDENCY_MODES = ${json(contract.modes)} as const;

export type DependencyMode = (typeof DEPENDENCY_MODES)[number];

export const ACTIVE_LOCK_NAME = ${json(contract.activeLockFileName)};
export const TEMPLATE_LOCK_NAME = ${json(contract.templateLockFileName)};
export const WORKSPACE_LOCK_NAME = ${json(contract.workspaceLockName)};
export const WORKSPACE_LOCK_PROTOCOL = ${json(contract.workspaceLockProtocol)} as const;
export const LEGACY_DEPENDENCY_ENTRY_FIELDS = ${json(contract.legacyDependencyEntryFields)} as const;
export const DEPENDENCY_ENTRY_FIELDS = ${json(contract.dependencyEntryFields)} as const;
export const REQUIRED_DEPENDENCY_ENTRY_FIELDS = ${json(contract.requiredDependencyEntryFields)} as const;
export const OPTIONAL_DEPENDENCY_ENTRY_FIELDS = ${json(contract.optionalDependencyEntryFields)} as const;
export const REMOVED_TOP_LEVEL_FIELDS = ${json(contract.removedTopLevelFields)} as const;
export const SAFE_DEPENDENCY_NAME_PATTERN_SOURCE = ${json(contract.safeDependencyNamePattern)};

export const LOCK_FIELDS = ${json(contract.fields)} as const;

export const LOCK_SCHEMA_CONTRACT = {
  schemaVersion: LOCK_SCHEMA_VERSION,
  modes: DEPENDENCY_MODES,
  activeLockFileName: ACTIVE_LOCK_NAME,
  templateLockFileName: TEMPLATE_LOCK_NAME,
  workspaceLockName: WORKSPACE_LOCK_NAME,
  workspaceLockProtocol: WORKSPACE_LOCK_PROTOCOL,
  legacyDependencyEntryFields: LEGACY_DEPENDENCY_ENTRY_FIELDS,
  dependencyEntryFields: DEPENDENCY_ENTRY_FIELDS,
  requiredDependencyEntryFields: REQUIRED_DEPENDENCY_ENTRY_FIELDS,
  optionalDependencyEntryFields: OPTIONAL_DEPENDENCY_ENTRY_FIELDS,
  removedTopLevelFields: REMOVED_TOP_LEVEL_FIELDS,
  safeDependencyNamePattern: SAFE_DEPENDENCY_NAME_PATTERN_SOURCE,
  fields: LOCK_FIELDS,
} as const;

const SAFE_DEPENDENCY_NAME_PATTERN = new RegExp(
  SAFE_DEPENDENCY_NAME_PATTERN_SOURCE,
);

export function dependencyMode(value: unknown): DependencyMode | undefined {
  return DEPENDENCY_MODES.find((mode) => mode === value);
}

export function isSafeDependencyName(name: string): boolean {
  return SAFE_DEPENDENCY_NAME_PATTERN.test(name);
}
`;
}

function validateActivationEvents(contract) {
  const manifest = JSON.parse(fs.readFileSync(packagePath, "utf8"));
  const activationEvents = manifest.activationEvents;
  if (!Array.isArray(activationEvents)) {
    throw new Error("package.json activationEvents must be an array");
  }
  for (const lockName of [
    contract.activeLockFileName,
    contract.templateLockFileName,
  ]) {
    const expected = `workspaceContains:${lockName}`;
    if (!activationEvents.includes(expected)) {
      throw new Error(
        `package.json activationEvents is missing canonical lock event ${expected}`,
      );
    }
  }
}

const args = new Set(process.argv.slice(2));
if ([...args].some((arg) => arg !== "--check")) {
  throw new Error(`Unsupported arguments: ${[...args].join(" ")}`);
}

const contract = JSON.parse(fs.readFileSync(contractPath, "utf8"));
validateActivationEvents(contract);
const expected = render(contract);
if (args.has("--check")) {
  const actual = fs.readFileSync(outputPath, "utf8");
  if (actual !== expected) {
    console.error(
      "src/lockSchema.ts is stale; run npm run generate:lock-schema and commit the result.",
    );
    process.exitCode = 1;
  }
} else {
  fs.writeFileSync(outputPath, expected, "utf8");
  console.log(`Generated ${path.relative(repositoryRoot, outputPath)}`);
}
